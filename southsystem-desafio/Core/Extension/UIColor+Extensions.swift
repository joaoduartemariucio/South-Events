import UIKit

extension UIColor {
    
    static var colorPrimary: UIColor {
        return UIColor(red: 0.95, green: 0.22, blue: 0.04, alpha: 1.00)
    }
    
    static var colorPrimaryLight: UIColor {
        return UIColor(red: 1.00, green: 0.44, blue: 0.24, alpha: 1.00)
    }
    
    static var colorPrimaryDark: UIColor {
        return UIColor(red: 0.71, green: 0.00, blue: 0.00, alpha: 1.00)
    }
}
