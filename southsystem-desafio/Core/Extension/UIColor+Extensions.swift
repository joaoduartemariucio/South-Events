import UIKit

extension UIColor {
    
    static var colorPrimary: UIColor {
        return UIColor(red: 0.87, green: 0.04, blue: 0.03, alpha: 1.00)
    }
    
    static var colorPrimaryLight: UIColor {
        return UIColor(red: 1.00, green: 0.34, blue: 0.22, alpha: 1.00)
    }
    
    static var colorPrimaryDark: UIColor {
        return UIColor(red: 0.64, green: 0.00, blue: 0.00, alpha: 1.00)
    }
}
